#include <unistd.h>

int	main(int argc, char **argv)
{
	int	j;
	int	i;

	j = argc - 1;
	if (argc > 1)
	{
		while (j >= 1)
		{
			i = 0;
			while (argv[j][i])
			{
				write(1, &argv[j][i], 1);
				i++;
			}
			j--;
			write(1, "\n", 1);
		}
	}
}
